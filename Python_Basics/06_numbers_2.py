#!/usr/bin/env python3.9



# Division always returns a floating point number. 
print ( 17 / 3 )    # 5.666666666666667


# There is full support  for floating point; operators with mixed type 
# operands convert the integer operand to floating point.
print ( 4 * 3.75 - 1 )  # 14.0


# To do floor division and get an integer result you can  use the // operator. 
print ( 17 // 3 )   # 5


# To calculate the remainder you can use %.
print ( 17 % 3 )    # 2


# It is possible to use the ** operator to calculate powers.
print ( 3 ** 3 )    # 27


# In addition to int and float, Python supports other types of numbers, 
# Decimal and Fraction. 
# Python also has built-in support for complex numbers, and uses the j or J 
# suffix to indicate the imaginary part (e.g. 3+5j).






